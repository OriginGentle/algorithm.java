    // 根据'e'或者'E'作为分隔符
    // 分成左右两个部门分别考察是否合法
    public static boolean isNumber1(String s) {
        int n = s.length();
        char[] str = s.toCharArray();
        int index = -1;
        for (int i = 0; i < n; i++) {
            if (str[i] == 'e' || str[i] == 'E') {
                if (index == -1) index = i;
                else return false;
            }
        }
        boolean ans = true;
        if (index != -1) {
            ans &= process(str, 0, index - 1, false);
            ans &= process(str, index + 1, n - 1, true);
        } else {
            ans &= process(str, 0, n - 1, false);
        }
        return ans;
    }

    public static boolean process(char[] cs, int start, int end, boolean mustInteger) {
        if (start > end) return false;
        if (cs[start] == '+' || cs[start] == '-') start++;
        boolean hasDot = false, hasNum = false;
        for (int i = start; i <= end; i++) {
            if (cs[i] == '.') {
                if (mustInteger || hasDot) return false;
                hasDot = true;
            } else if (cs[i] >= '0' && cs[i] <= '9') {
                hasNum = true;
            } else {
                return false;
            }
        }
        return hasNum;
    }

